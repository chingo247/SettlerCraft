package com.chingo247.structureapi.persistence.dao.placement;

/**
 *
 * @author Chingo
 */
public class DefaultPlacementData implements PlacementData {
    
    private final int width;
    private final int height;
    private final int length;
    private final String type;

    DefaultPlacementData(PlacementDataNode pdn) {
        this.width = pdn.getLength();
        this.length = pdn.getLength();
        this.height= pdn.getHeight();
        this.type = pdn.getType();
    }

    @Override
    public int getWidth() {
        return width;
    }

    @Override
    public int getHeight() {
        return height;
    }

    @Override
    public int getLength() {
        return length;
    }

    @Override
    public String getType() {
        return type;
    }
    
}
